
package beginerjava;

public class AssignmentDemo {
    public static void main(String[] args) {
        int x=20;
        int y=10;
        x+=y;//x=x+y(20+10)=30
        System.out.println("Output :"+x);
        x-=y;//x=x-y(20-10)=10
        System.out.println("Output :"+x);
        x*=y;//x=x*y(20*10)=200
        System.out.println("Output :"+x);
        x/=y;//x=x/y(20/10)=2
        System.out.println("Output :"+x);
        x%=y;//x=x%y(20%10)=0
        System.out.println("Output :"+x);
    }
    
}
